import torch
from torch.utils.data import DataLoader,Dataset
from torchvision import transforms
import glob
import random
from PIL import Image
import os

class ImageDataset(Dataset):
    def __init__(self, root, transform=None, mode='train'):
        self.transform = transform
        self.files_A = sorted(glob.glob(os.path.join(root, 'A' ) + '/*.*'))
        self.files_B = sorted(glob.glob(os.path.join(root, 'B' ) + '/*.*'))
        if len(self.files_A) > len(self.files_B):
            self.files_A, self.files_B = self.files_B, self.files_A
        self.new_perm()
        assert len(self.files_A) > 0

    def new_perm(self):
        self.randperm = torch.randperm(len(self.files_B))[:len(self.files_A)]

    def __getitem__(self, index):
        item_A = self.transform(Image.open(self.files_A[index % len(self.files_A)]))
        item_B = self.transform(Image.open(self.files_B[self.randperm[index]]))
        if item_A.shape[0] != 3:
            item_A = item_A.repeat(3, 1, 1)
        if item_B.shape[0] != 3:
            item_B = item_B.repeat(3, 1, 1)
        if index == len(self) - 1:
            self.new_perm()

        return (item_A - 0.5) * 2, (item_B - 0.5) * 2

    def __len__(self):
        return min(len(self.files_A), len(self.files_B))
    
def create_Dataloader(folder,resize,batchsize=10,shuffle=True):
    transform = transforms.Compose([
    transforms.Resize(resize),
    transforms.ToTensor(),
    ])
    
    return(DataLoader(ImageDataset(folder,transform=transform),batch_size=batchsize,shuffle=shuffle))
